DROP TABLE IF EXISTS DOLLS;

CREATE TABLE DOLLS (
  ID INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  YEAR_CREATE INT NOT NULL,
  BRAND VARCHAR(256) NOT NULL,
  RULER VARCHAR(256) NOT NULL,
  SERIES VARCHAR(256) NOT NULL,
  NAME_PERSON VARCHAR(256),
  DESCRIPTION VARCHAR(1024),
  PRICE BIGINT,
  PROM_IMAGE VARCHAR(128),
  MY_IMAGE VARCHAR(128),
  PRIMARY KEY (ID)
);